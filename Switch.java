class Switch {
    public static void main(String args[]){
        String  colour="white";
        switch(colour){
            case "red":
                        System.out.println("My colour is red");
                        break;
            case "green":
                        System.out.println("My colour is green");
                        break;
            case "yellow": 
                         System.out.println("My colour is red");
                        break;
            case "blue":
                        System.out.println("My colour is blue");
                        break;
            case "orange": 
                        System.out.println("My colour is orange");
                        break;
            case "pink":
                        System.out.println("My colour is pink");
                        break;
            case "violet":
                        System.out.println("My colour is violet");
                        break;
            case "black":
                        System.out.println("My colour is black");
                        break;
                 default:
                         System.out.println("My colour is not found");
                         

        }                
    }
}
